// สูตรการคำนวณ: การวิเคราะห์และปันส่วนต้นทุนค่ารักษาพยาบาล (Hospital Treatment Cost Apportionment)
// =========================================================================================
// ไฟล์นี้ใช้ปันส่วนต้นทุนค่ารักษาพยาบาลระหว่างผู้ป่วยนอก (OPD) และผู้ป่วยใน (IPD) 
// โดยพิจารณาจากสัดส่วนของรายได้รวม และจำแนกประเภทรายจ่ายหลักออกเป็น: Labor Cost (LC), Material Cost (MC), และ Capital Cost (CC)
//
// แหล่งข้อมูลและรหัสผังบัญชี (COA Codes) ที่ใช้ปันส่วน:
//   1. OPD_REV (รายได้ผู้ป่วยนอก):
//      - รหัสบัญชีรายได้ค่ารักษาทางตรงที่เกิดจากกลุ่มผู้ป่วยนอก (OPD) เช่น 4301020102.102, 4301020102.104 เป็นต้น
//   2. IPD_REV (รายได้ผู้ป่วยใน):
//      - รหัสบัญชีรายได้ค่ารักษาทางตรงที่เกิดจากกลุ่มผู้ป่วยใน (IPD) เช่น 4301020104.105, 4301020104.107 เป็นต้น
//   3. LC (Labor Cost - ต้นทุนค่าแรง):
//      - เงินเดือน, ค่าจ้างประจำ, ค่าตอบแทนบุคลากร, และค่าสวัสดิการทางตรง (รหัส 510101*, 510102*, 510404*)
//   4. MC (Material Cost - ต้นทุนค่าวัสดุเวชภัณฑ์):
//      - ต้นทุนยา, ต้นทุนวัสดุเวชภัณฑ์มิใช่ยา, วัสดุวิทยาศาสตร์การแพทย์, และวัสดุสิ้นเปลือง (รหัส 510401*, 510402*, 510403*)
//   5. CC (Capital Cost - ต้นทุนค่าลงทุนดำเนินงาน):
//      - ค่าบำรุงรักษา, ค่าเช่าอาคาร/ครุภัณฑ์, และค่าสาธารณูปโภคขั้นพื้นฐาน (รหัส 510501*)
//   6. OTHER_EXP / OTHER_REV (รายจ่าย/รายได้อื่นๆ ที่ไม่เกี่ยวข้องกับการรักษาพยาบาลโดยตรง)
//
// วิธีการคำนวณ (Calculation Logic):
//   1. หาผลรวมรายได้ OPD (opdRev) และ IPD (ipdRev) สะสม YTD
//   2. หาผลรวมต้นทุนบริการรวม (baseCost) = LC + MC + CC
//   3. คำนวณสัดส่วนและปันส่วนต้นทุน (Cost Apportionment):
//      - ต้นทุน OPD (opdCost) = (opdRev / (opdRev + ipdRev)) * baseCost
//      - ต้นทุน IPD (ipdCost) = (ipdRev / (opdRev + ipdRev)) * baseCost
//   4. อ้างอิงสถิติโรงพยาบาล (VisitOPD / AdjRW) เพื่อหาราคาต่อครั้ง/ต่อกลุ่มวินิจฉัยโรคร่วม (AdjRW)
// =========================================================================================


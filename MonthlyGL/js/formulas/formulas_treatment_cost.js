function calculateTreatmentCostMetricsForMonth(month) {
    const tbData = month.tbData || [];

    const opdRev = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.OPD_REV);
    const ipdRev = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.IPD_REV);
    const lc = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.LC);
    const mc = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.MC);
    const cc = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.CC);
    const otherExp = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.OTHER_EXP);
    const otherRev = sumGLAccountsHelper(tbData, TREATMENT_COST_CONFIG.OTHER_REV);

    const totalRev = opdRev + ipdRev;
    const baseCost = lc + mc + cc;

    let opdCost = 0;
    let ipdCost = 0;
    if (totalRev > 0) {
        opdCost = (opdRev / totalRev) * baseCost;
        ipdCost = (ipdRev / totalRev) * baseCost;
    }

    return {
        opdRev,
        ipdRev,
        totalRev,
        lc,
        mc,
        cc,
        baseCost,
        otherExp,
        otherRev,
        opdCost,
        ipdCost
    };
}

function sumGLAccountsHelper(tbData, codes) {
    let sum = 0;
    if (!tbData) return 0;
    tbData.forEach(row => {
        const rowCode = String(row.code).trim();
        if (codes.includes(rowCode)) {
            const classChar = rowCode.charAt(0);
            const dr = Number(row.end_dr) || 0;
            const cr = Number(row.end_cr) || 0;
            if (classChar === '1' || classChar === '5') {
                sum += (dr - cr);
            } else if (classChar === '2' || classChar === '3' || classChar === '4') {
                sum += (cr - dr);
            } else {
                sum += (dr - cr);
            }
        }
    });
    return sum;
}

function getHospitalValue(sheetName, dateObj) {
    if (!window.hospitalData || !window.hospitalData[sheetName]) return null;
    const calendarYear = dateObj.getFullYear();
    const monthIndex = dateObj.getMonth();
    const fiscalYearBE = (monthIndex >= 9) ? (calendarYear + 1 + 543) : (calendarYear + 543);
    
    const yearData = window.hospitalData[sheetName][fiscalYearBE];
    if (!yearData) return null;
    return yearData[monthIndex] !== undefined ? yearData[monthIndex] : null;
}

